uint8_t syncNTPTime(void)
{
    Serial.begin(115200);
    DEBUG_PRINT("\nConnecting WiFi...");
    
    WiFi.begin(WIFI_SSID, WIFI_PASS);
    int wifiRetry = 0;
    while (WiFi.status() != WL_CONNECTED && wifiRetry < 20) {
        delay(500);
        DEBUG_PRINT(".");
        wifiRetry++;
    }

    if (WiFi.status() != WL_CONNECTED) {
        DEBUG_PRINTLN("\nWiFi connection failed!");
        return 0;
    }
    DEBUG_PRINTLN("\nWiFi connected");

    configTime(TIME_ZONE * 3600, 0, "ntp.aliyun.com", "time.ustc.edu.cn");
    
    time_t now = time(nullptr);
    int timeRetry = 0;
    while (now < 1600000000 && timeRetry < 20) {
        delay(500);
        now = time(nullptr);
        DEBUG_PRINT("\nSyncing time...");
        timeRetry++;
    }

    if (now >= 1600000000) {
        DEBUG_PRINTLN("\nTime sync complete");
    } else {
        DEBUG_PRINTLN("\nTime sync failed!");
    }
    
    return WiFi.channel();
}

void getCurrentTime(int& hour, int& minute, int& second)
{
    time_t now = time(nullptr);
    struct tm timeinfo = *localtime(&now);
    hour = timeinfo.tm_hour;
    minute = timeinfo.tm_min;
    second = timeinfo.tm_sec;
    DEBUG_PRINTF("\nCurrent time: %02d:%02d:%02d\n", hour, minute, second);
}

bool isNetworkReady()
{
    HTTPClient http;
    http.begin("http://www.baidu.com");
    http.setTimeout(3000);
    int code = http.GET();
    http.end();
    return (code == 200);
}

int WIFI_signal(void)
{
    if (WiFi.status() != WL_CONNECTED) {
        DEBUG_PRINTLN("\nWiFi not connected, cannot read signal strength");
        return 0;
    }

    int retry = 0;
    while (!isNetworkReady() && retry < 10) {
        DEBUG_PRINTF("\nWiFi connected but network not ready, retry %d/10...\n", retry + 1);
        delay(1000);
        retry++;
    }

    int rssi = WiFi.RSSI();
    DEBUG_PRINTF("\nWiFi signal strength (RSSI): %d dBm\n", rssi);
    
    return rssi;
}

